# Numeric literals
x = 24
y = 24.3
z = 2+3j
print(x, y, z)


# string literals
 
s = 'Yashaswini'             # in single quote
t = "yashaswini"             # in double quotes
m = '''y
           for
              yashaswini ''' # multi-line String
 
print(s)
print(t)
print(m)

# character literal 

v = 'n'                     # in single quote
w = "a"                     # in double quotes
 
print(v)
print(w)

# Complex literal

z = 7 + 5j
k = 7j
 
print(z, k)

